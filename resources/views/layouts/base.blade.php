    <script>
        $(window).on('load', function () {
            $('#postTable').removeAttr('style');
            toastr.options.closeButton = true;
            toastr.options.showMethod = 'slideDown';
            toastr.options.hideMethod = 'slideUp';
            toastr.options.closeMethod = 'slideUp';
            toastr.options.timeOut = 3500;
            toastr.options.closeDuration = 250;
        })
    </script>

    <script type="text/javascript">
        // add a new task
        $(document).on('click', '.add-modal', function () {
            $('.modal-title').text('Add');
            $('#addModal').modal('show');
        });
        $('.modal-footer').on('click', '.add', function () {
            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });
            $.ajax({
                type: 'POST',
                url: 'add-task',
                data: {
                    'name': $('#name_add').val(),
                    'description': $('#description_add').val(),
                    'user_id': {{ Auth::user()->id}},
                    'status': 0,
                    'assign': 0
                },
                success: function (data) {
                    $('.errorName').addClass('hidden');
                    $('.errorDescription').addClass('hidden');

                    if ((data.errors)) {
                        setTimeout(function () {
                            $('#addModal').modal('show');
                            toastr.error('Validation error!', "Error");
                        }, 500);

                        if (data.errors.name) {
                            $('.errorName').removeClass('hidden');
                            $('.errorName').text(data.errors.name);
                        }
                        if (data.errors.description) {
                            $('.errorDescription').removeClass('hidden');
                            $('.errorDescription').text(data.errors.description);
                        }
                    } else {
                        toastr.success('Task added!', "Success");
                        $('#postTable').append("<tr class='item" + data.id + "'><td>" + data.id + "</td><td>" + data.name + "</td><td>" + data.description + "</td><td>" + data.created_at + " </td><td>" + data.updated_at + " </td><td><button class='edit-modal btn btn-info' data-id='" + data.id + "' data-name='" + data.name + "' data-description='" + data.description + "'> <span class='glyphicon glyphicon-edit'></span> Edit</button> <button class='delete-modal btn btn-danger' data-id='" + data.id + "' data-name='" + data.name + "' data-description='" + data.description + "'> <span class='glyphicon glyphicon-trash'></span> Delete</button></td></tr>");
                    }
                },
            });
        });

        // Edit a post
        $(document).on('click', '.edit-modal', function () {
            $('.modal-title').text('Edit');
            $('#id_edit').val($(this).data('id'));
            $('#name_edit').val($(this).data('name'));
            $('#description_edit').val($(this).data('description'));
            id = $('#id_edit').val();
            $('#editModal').modal('show');
        });
        $('.modal-footer').on('click', '.edit', function () {
            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });
            $.ajax({
                type: 'PUT',
                url: '/edit-task/' + id,
                data: {
                    '_token': $('input[name=_token]').val(),
                    'id': $("#id_edit").val(),
                    'name': $('#name_edit').val(),
                    'description': $('#description_edit').val()
                },
                success: function (data) {
                    $('.errorName').addClass('hidden');
                    $('.errorDescription').addClass('hidden');

                    if ((data.errors)) {
                        setTimeout(function () {
                            $('#editModal').modal('show');
                            toastr.error('Validation error!');
                        }, 500);

                        if (data.errors.name) {
                            $('.errorTitle').removeClass('hidden');
                            $('.errorTitle').text(data.errors.name);
                        }
                        if (data.errors.description) {
                            $('.errorDescription').removeClass('hidden');
                            $('.errorDescription').text(data.errors.description);
                        }
                    } else {
                        toastr.success('Task updated!', "Success");
                        $('.item' + data.id).replaceWith("<tr class='item" + data.id + "'><td>" + data.id + "</td><td>" + data.name + "</td><td>" + data.description + "</td><td>" + data.created_at + " </td><td>" + data.updated_at + " </td><td><button class='edit-modal btn btn-info' data-id='" + data.id + "' data-name='" + data.name + "' data-description='" + data.description + "'> <span class='glyphicon glyphicon-edit'></span> Edit</button> <button class='delete-modal btn btn-danger' data-id='" + data.id + "' data-name='" + data.name + "' data-description='" + data.description + "'> <span class='glyphicon glyphicon-trash'></span> Delete</button></td></tr>");
                    }
                },
            });
        });

        //delete a task
        $(document).on('click', '.delete-modal', function () {
            $('.modal-title').text('Delete');
            $('#id_delete').val($(this).data('id'));
            $('#name_delete').val($(this).data('name'));
            $('#deleteModal').modal('show');
            id = $('#id_delete').val();
        });
        $('.modal-footer').on('click', '.delete', function () {
            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });
            $.ajax({
                type: 'DELETE',
                url: '/delete-task/' + id,
                data: {},
                success: function (data) {
                    toastr.success('Task deleted!', "Success");
                    $('.item' + id).remove();
                }
            });
        });
    </script>

